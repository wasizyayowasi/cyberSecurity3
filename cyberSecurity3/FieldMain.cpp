//マップ描画
void FieldMain::drawMap(int offsetX, int offsetY) {

	//マップデータの描画①
	for (int x = 0; x < kBgNumX; x++) {
		for (int y = 0; y < kBgNumY; y++) {

			const int chipNo = kMapData[y][x];

			assert(chipNo >= 0);
			assert(chipNo < chipNum());

			int graphX = (chipNo % chipNumX()) * kChipSize;
			int graphY = (chipNo / chipNumX()) * kChipSize;

			DrawRectGraph(x * kChipSize + offsetX, y * kChipSize + offsetY,
				graphX, graphY, kChipSize, kChipSize,
				m_hWall, true, false);
		}
	}

	//マップデータの描画②
	for (int x = 0; x < kBgNum2X; x++) {
		for (int y = 0; y < kBgNum2Y; y++) {

			const int chipNo2 = kMapData2[y][x];

			assert(chipNo2 >= 0);
			assert(chipNo2 < chipNum2());


			int graph2X = (chipNo2 % chipNum2X()) * kChipSize;
			int graph2Y = (chipNo2 / chipNum2X()) * kChipSize;

			DrawRectGraph(x * kChipSize + offsetX, y * kChipSize + offsetY,
				graph2X, graph2Y, kChipSize, kChipSize,
				m_hOrnament, true, false);
		}
	}

	//マップデータの描画③
	for (int x = 0; x < kBgNum3X; x++) {
		for (int y = 0; y < kBgNum3Y; y++) {
			const int chipNo3 = kMapData3[y][x];

			if (changeFieldNum != 2) {
				assert(chipNo3 >= 0);
				assert(chipNo3 < chipNum2());

				int graph3X = (chipNo3 % chipNum2X()) * kChipSize;
				int graph3Y = (chipNo3 / chipNum2X()) * kChipSize;

				DrawRectGraph(x * kChipSize + offsetX, y * kChipSize + offsetY,
					graph3X, graph3Y, kChipSize, kChipSize,
					m_hOrnament, true, false);
			}
			else {
				assert(chipNo3 >= 0);
				assert(chipNo3 < chipNum3());

				int graph3X = (chipNo3 % chipNum3X()) * kChipSize;
				int graph3Y = (chipNo3 / chipNum3X()) * kChipSize;

				DrawRectGraph(x * kChipSize + offsetX, y * kChipSize + offsetY,
					graph3X, graph3Y, kChipSize, kChipSize,
					m_hOrnament2, true, false);
			}
		}
	}
}


