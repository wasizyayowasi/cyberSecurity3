void FieldMain::changeField()
{

	switch (changeFieldNum) {
	case 0:
		mainFieldLoad();
		break;
	case 1:
		searchEngineRoom();
		break;
	case 2:
		referenceRoom();
		break;
	case 3:
		corridor();
		break;
	case 4:
		corridor2();
		break;
	case 5:
		corridor3();
		break;
	}
}



void FieldMain::update()
{
	Pad::update();

	switch (changeFieldNum) {
	case 0:
		mainFieldUpdate();
		break;
	case 1:
		searchEngineRoomUpdate();
		break;
	case 2:
		referenceRoomUpdate();
		break;
	case 3:
		corridorUpdate();
		break;
	case 4:
		corridor2Update();
		break;
	case 5:
		corridor3Update();
		break;
	case 6:
		changeFieldNum = m_pc.update();
		break;
	case 7:
		changeFieldNum = m_referenceRoom.update();
		break;
	}
	if (changeFieldNum < 6) {
		m_player.trace();
		m_player.update();
	}
}


